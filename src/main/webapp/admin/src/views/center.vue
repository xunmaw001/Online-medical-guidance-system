<script>
// 数字，邮件，手机，url，身份证校验
import { isNumber,isIntNumer,isEmail,isMobile,isPhone,isURL,checkIdCard } from "@/utils/validate";

export default {
  data() {
    return {
      ruleForm: {},
      flag: '',
      usersFlag: false,
                                                                  yonghuxingbieOptions: [],
                                                                                                                                                yishengsuoshuyiyuanOptions: [],
                        yishengkeshiOptions: [],
                        yishengzhichengOptions: [],
                        yishengxingbieOptions: [],
                                                                                                                                                                                        };
  },
  mounted() {
    var table = this.$storage.get("sessionTable");
    this.flag = table;
    this.$http({
      url: `${this.$storage.get("sessionTable")}/session`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.ruleForm = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
                                            this.yonghuxingbieOptions = "男,女".split(',')
                                                                                                this.$http({
      url: `option/yiyuanxinxi/yiyuanmingcheng`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.yishengsuoshuyiyuanOptions = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
                this.$http({
      url: `option/keshiliebiao/keshi`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.yishengkeshiOptions = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
                this.$http({
      url: `option/zhichengliebiao/zhicheng`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.yishengzhichengOptions = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
                this.yishengxingbieOptions = "男,女".split(',')
                                                                                                                          },
  methods: {
                                                                                                                                                                                                                                                                                                                                                                yonghugerenzhaopianUploadChange(fileUrls) {
        this.ruleForm.gerenzhaopian = fileUrls;
    },
                                                                                                        yishengtouxiangUploadChange(fileUrls) {
        this.ruleForm.touxiang = fileUrls;
    },
                                                                                                    onUpdateHandler() {
                              if((!this.ruleForm.yonghuming)&& 'yonghu'==this.flag){
        this.$message.error('用户名不能为空');
        return
      }
                                                                  if((!this.ruleForm.mima)&& 'yonghu'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
                                                                  if((!this.ruleForm.xingming)&& 'yonghu'==this.flag){
        this.$message.error('姓名不能为空');
        return
      }
                                                                                                                              if( 'yonghu' ==this.flag && this.ruleForm.nianling&&(!isIntNumer(this.ruleForm.nianling))){
       this.$message.error(`年龄应输入整数`);
        return
      }
                                                                                    if( 'yonghu' ==this.flag && this.ruleForm.lianxidianhua&&(!isMobile(this.ruleForm.lianxidianhua))){
        this.$message.error(`联系电话应输入手机格式`);
        return
      }
                                                                        if( 'yonghu' ==this.flag && this.ruleForm.dianziyouxiang&&(!isEmail(this.ruleForm.dianziyouxiang))){
        this.$message.error(`电子邮箱应输入邮箱格式`);
        return
      }
                                                                        if( 'yonghu' ==this.flag && this.ruleForm.shenfenzhenghao&&(!checkIdCard(this.ruleForm.shenfenzhenghao))){
        this.$message.error(`身份证号应输入身份证格式`);
        return
      }
                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                            if( 'yisheng' ==this.flag && this.ruleForm.yiling&&(!isIntNumer(this.ruleForm.yiling))){
       this.$message.error(`医龄应输入整数`);
        return
      }
                                                                                    if( 'yisheng' ==this.flag && this.ruleForm.lianxidianhua&&(!isMobile(this.ruleForm.lianxidianhua))){
        this.$message.error(`联系电话应输入手机格式`);
        return
      }
                                                                                                                                                                                                                                                                                                                                                                                                      this.$http({
        url: `${this.$storage.get("sessionTable")}/update`,
        method: "post",
        data: this.ruleForm
      }).then(({ data }) => {
        if (data && data.code === 0) {
          this.$message({
            message: "修改信息成功",
            type: "success",
            duration: 1500,
            onClose: () => {
            }
          });
        } else {
          this.$message.error(data.msg);
        }
      });
    }
  }
};
</script>
